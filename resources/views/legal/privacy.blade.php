    <h1>Политика конфиденциальности</h1>
    <p class="muted">Приложение «OpenSales» · Последнее обновление: 22.06.2026</p>

    <p>Настоящая Политика конфиденциальности описывает, как мобильное приложение
    «OpenSales» (далее — «Приложение»), оператором которого является
    <strong>«OpenSales team»</strong> (далее — «мы»), собирает,
    использует и защищает персональные данные пользователей.</p>

    <h2>1. Какие данные мы собираем</h2>
    <ul>
        <li><strong>Номер телефона</strong> — для входа и идентификации аккаунта (вход по SMS-коду / OTP).</li>
        <li><strong>Аккаунт Telegram</strong> — при добровольной привязке для входа и уведомлений.</li>
        <li><strong>Геолокация</strong> — только во время использования приложения, чтобы показать ближайших
            продавцов (дилеров) и выбрать точку при регистрации. В фоновом режиме местоположение не отслеживается.</li>
        <li><strong>Камера</strong> — только для сканирования QR-кода приглашения. Изображения с камеры не
            сохраняются и не передаются.</li>
        <li><strong>Данные заказов</strong> — выбранные товары, количество, суммы, статус и история заказов.</li>
        <li><strong>Токен push-уведомлений</strong> — идентификатор устройства (через Firebase Cloud Messaging)
            для доставки уведомлений о заказах и сообщениях.</li>
        <li><strong>Технические данные</strong> — модель устройства, версия ОС, версия приложения — для
            стабильной работы и устранения ошибок.</li>
    </ul>

    <h2>2. Как мы используем данные</h2>
    <ul>
        <li>Создание и защита вашего аккаунта;</li>
        <li>Оформление и сопровождение заказов у дилеров;</li>
        <li>Показ ближайших дилеров на карте;</li>
        <li>Отправка уведомлений о статусе заказа и сообщениях;</li>
        <li>Улучшение работы и безопасность Приложения.</li>
    </ul>

    <h2>3. Передача данных третьим лицам</h2>
    <p>Мы не продаём персональные данные. Данные могут обрабатываться следующими сервисами
    исключительно для работы Приложения:</p>
    <ul>
        <li><strong>Firebase (Google)</strong> — доставка push-уведомлений;</li>
        <li><strong>Дилер (продавец)</strong>, у которого вы оформляете заказ — получает данные заказа
            (товары, количество, контакт), необходимые для его выполнения;</li>
        <li>Картографические тайлы (OpenStreetMap) — для отображения карты.</li>
    </ul>
    <p>Данные могут быть раскрыты по требованию закона.</p>

    <h2>4. Хранение и защита</h2>
    <p>Данные хранятся на защищённых серверах и используются только в течение времени, необходимого
    для предоставления услуг. Токены доступа хранятся в защищённом хранилище устройства.</p>

    <h2>5. Ваши права</h2>
    <ul>
        <li>Запросить доступ к вашим данным или их исправление;</li>
        <li>Удалить аккаунт и связанные с ним данные;</li>
        <li>Отключить уведомления и доступ к геолокации в настройках устройства.</li>
    </ul>
    <p>Для удаления аккаунта напишите нам на адрес ниже — мы удалим данные в разумный срок.</p>

    <h2>6. Дети</h2>
    <p>Приложение не предназначено для лиц младше 13 лет, и мы сознательно не собираем их данные.</p>

    <h2>7. Изменения</h2>
